public class AboutUs {

    private String law_statement;

    private String privacy_protection;

    private String website_map;

    private String contact_us;

    public String getLawStatement() {
        return law_statement;
    }

    public void setLawStatement(String law_statement) {
        this.law_statement = law_statement;
    }

    public String getPrivacyProtection() {
        return privacy_protection;
    }

    public void setPrivacyProtection(String privacy_protection) {
        this.privacy_protection = privacy_protection;
    }

    public String getWebsiteMap() {
        return website_map;
    }

    public void setWebsiteMap(String website_map) {
        this.website_map = website_map;
    }

    public String getContactUs() {
        return contact_us;
    }

    public void setContactUs(String contact_us) {
        this.contact_us = contact_us;
    }
}
